#pragma once

#include <cstdint>

namespace Ciallang::Common {

    inline uint64_t align(const uint64_t size, const uint64_t align) {
        if(align > 0) {
            const auto result = size + align - 1;
            return result - result % align;
        }
        return size;
    }
}
